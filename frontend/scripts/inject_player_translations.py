# --- Driver ------------------------------------------------------------------


def main() -> None:
    locales = list(VIDEO.keys())
    msg_dir = Path(__file__).resolve().parent.parent / "messages"
    if not msg_dir.is_dir():
        raise SystemExit(f"Could not find messages dir at {msg_dir}")

    for loc in locales:
        path = msg_dir / f"{loc}.json"
        if not path.is_file():
            print(f"[skip] {path.name} not found")
            continue
        with path.open(encoding="utf-8") as f:
            data = json.load(f)
        data["videoPlayerControls"] = VIDEO[loc]
        data["questLog"] = QUEST[loc]
        with path.open("w", encoding="utf-8") as f:
            json.dump(data, f, indent=2, ensure_ascii=False)
            f.write("\n")
        print(f"[ok]   {path.name}: +videoPlayerControls (+{len(VIDEO[loc])}) "
              f"+questLog (+{len(QUEST[loc])})")


if __name__ == "__main__":
    main()
