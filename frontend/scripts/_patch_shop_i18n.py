def patch_locale(locale: str) -> None:
    path = MESSAGES_DIR / f"{locale}.json"
    if not path.exists():
        print(f"skip {locale}.json — missing")
        return
    data = json.loads(path.read_text(encoding="utf-8"))
    if locale not in SHOP_TRANSLATIONS:
        print(f"skip {locale}.json — no translations defined")
        return
    data["shop"] = SHOP_TRANSLATIONS[locale]
    path.write_text(
        json.dumps(data, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(f"patched {locale}.json")


def main() -> None:
    for locale in LOCALES:
        patch_locale(locale)


